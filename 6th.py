a = 123456789  # Integer

b = "Pikachu"  # String

c = True  # Boolean

d = None  # NoneType

e = 3.14  # Float

print(a)  # Output: 123456789
print(type(a))  # Output: <class 'int'>

print(b)  # Output: Pikachu
print(type(b))  # Output: <class 'str'>

print(c)  # Output: True
print(type(c))  # Output: <class 'bool'>

print(d)  # Output: None
print(type(d))  # Output: <class 'NoneType'>

print(e)  # Output: 3.14
print(type(e))  # Output: <class 'float'>