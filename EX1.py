# Exercise 1: Create a calculator that can perform basic arithmetic operations: addition, subtraction, multiplication, and division on two numbers.

# Solution:

a = 14

b = 84

# Addition
print("Addition:", a + b)  # Output: 98

# Subtraction
print("Subtraction:", a - b)  # Output: -70

# Multiplication
print("Multiplication:", a * b)  # Output: 1180

# Division
print("Division:", a / b)  # Output: 0.166666666666

# Modulus
print("Modulus:", a % b)  # Output: 14

# Floor Division
print("Floor Division:", a // b)  # Output: 0