# Exercise 1: Create a calculator that can perform basic arithmetic operations: addition, subtraction, multiplication, and division on two numbers.

# Solution:

a = 14

b = 2

# Addition
print("Addition:", a + b)  # Output: 16

# Subtraction
print("Subtraction:", a - b)  # Output: 12

# Multiplication
print("Multiplication:", a * b)  # Output: 28

# Division
print("Division:", a / b)  # Output: 7

# Modulus
print("Modulus:", a % b)  # Output: 0

# Floor Division
print("Floor Division:", a // b)  # Output: 7